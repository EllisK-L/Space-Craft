using System.Collections;
using UnityEngine;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


public class Lights : MonoBehaviour
{
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    public GameObject light0, light1;

    //--------------

    static WaitForSeconds delay0 = new WaitForSeconds(5.0f);


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //--------------

        StartCoroutine(LightsCH());

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    IEnumerator LightsCH()
    {
        //--------------

        while (true)
        {
            light0.SetActive(true);
            light1.SetActive(true);
            yield return delay0;
            light0.SetActive(false);
            light1.SetActive(true);
            yield return delay0;
            light0.SetActive(true);
            light1.SetActive(false);
            yield return delay0;
            light0.SetActive(false);
            light1.SetActive(false);
            yield return delay0;
        }

        //--------------
    }


    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}